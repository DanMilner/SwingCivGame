package game.map;

public class Coordinates {
    public int x;
    public int y;

    public Coordinates(int xCoordinate, int yCoordinate) {
        this.x = xCoordinate;
        this.y = yCoordinate;
    }

    public void setCoordinates(int xCoordinate, int yCoordinate) {
        this.x = xCoordinate;
        this.y = yCoordinate;
    }
}
